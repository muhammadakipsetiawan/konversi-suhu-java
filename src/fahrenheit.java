public class fahrenheit {
    public double tocalvin(double n){
        double k;
        k = (n + 456.67)/1.8;
        return k ;
                
    }public double tofahreinheit(double n){
        double k;
        k = n;
        return k ;
                
    }public double tocalcius(double n){
        double k;
        k = (n - 32)/1.8 ;
        return k ;
    }
}
